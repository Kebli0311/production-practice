# -*- coding: utf-8 -*-

"""
Программа: Калькулятор выполнения сменного задания (для отчётов производства)
Предприятие: АО «Сызранский НПЗ»
Функции:
1. Запрос названия продукта, планового объёма (тонн), фактического объёма (тонн)
2. Расчёт процента выполнения
3. Определение выполнения плана
4. Вывод результата в консоль
5. Добавление результата в файл дневного журнала
"""

import os
from datetime import datetime

def get_positive_float(prompt):
    """Циклически запрашивает у пользователя положительное число, пока ввод не станет корректен"""
    while True:
        try:
            value = float(input(prompt))
            if value <= 0:
                print("Значение должно быть положительным, повторите ввод.")
                continue
            return value
        except ValueError:
            print("Неверный ввод, введите число.")

def main():
    # Отображение заголовка программы
    print("=" * 60)
    print("     АО «Сызранский НПЗ» - Расчёт выполнения сменного задания")
    print("=" * 60)

    # Запрос названия продукта (не может быть пустым)
    while True:
        product_name = input("Введите название продукта (например, Бензин): ").strip()
        if product_name:
            break
        print("Название продукта не может быть пустым, повторите ввод.")

    # Запрос планового объёма (положительное число)
    plan = get_positive_float("Введите плановый объём (тонн): ")

    # Запрос фактического объёма (положительное число)
    fact = get_positive_float("Введите фактический объём (тонн): ")

    # Расчёт процента выполнения
    percent = (fact / plan) * 100

    # Определение статуса выполнения плана
    if percent >= 100:
        result_text = "План выполнен"
        result_message = f"Отлично! План выполнен на {percent:.2f}%."
    else:
        result_text = "План не выполнен"
        result_message = f"Внимание! План выполнен только на {percent:.2f}%. Требуется анализ причин."

    # Вывод результатов в консоль
    print("\n" + "-" * 40)
    print("Результаты расчёта:")
    print(f"Наименование продукта: {product_name}")
    print(f"Плановый объём: {plan:.2f} т")
    print(f"Фактический объём: {fact:.2f} т")
    print(f"Процент выполнения: {percent:.2f}%")
    print(f"Статус задания: {result_text}")
    print(result_message)
    print("-" * 40)

    # --- Запись результатов в файл журнала (по датам) ---
    today_str = datetime.now().strftime("%Y-%m-%d")
    filename = f"shift_report_{today_str}.txt"

    # Запись в режиме добавления (если файла нет, он создаётся автоматически)
    try:
        with open(filename, "a", encoding="utf-8") as f:
            f.write("\n" + "=" * 50 + "\n")
            f.write(f"Время записи: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}\n")
            f.write(f"Наименование продукта: {product_name}\n")
            f.write(f"Плановый объём: {plan:.2f} т, Фактический объём: {fact:.2f} т\n")
            f.write(f"Процент выполнения: {percent:.2f}%, Статус: {result_text}\n")
        print(f"\n✅ Запись добавлена в файл: {filename}")
    except IOError as e:
        print(f"\n❌ Ошибка записи в файл: {e}. Проверьте место на диске или права доступа.")

    print("\nРабота программы завершена.")

if __name__ == "__main__":
    main()